package tk.roccodev.zta.briefing.lergin;

public class NewMap {

	private String gameMode, name;

	public NewMap(String gameMode, String name) {
		this.gameMode = gameMode;
		this.name = name;
	}

	public String getGameMode() {
		return gameMode;
	}

	public String getName() {
		return name;
	}
	
	
	
}
